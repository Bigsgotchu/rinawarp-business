  /**
   * Load command aliases from file
   */
  loadAliases() {
    try {
      if (existsSync(this.aliasesFile)) {
        const data = readFileSync(this.aliasesFile, 'utf8');
        const aliases = JSON.parse(data);
        this.aliases = new Map(Object.entries(aliases));
      }
    } catch (error) {
      console.error('Error loading aliases:', error);
    }
  }

  /**
   * Save command aliases to file
   */
  saveAliases() {
    try {
      const aliasesObj = Object.fromEntries(this.aliases);
      writeFileSync(this.aliasesFile, JSON.stringify(aliasesObj, null, 2));
    } catch (error) {
      console.error('Error saving aliases:', error);
    }
  }

  /**
   * Load command history from file
   */
  loadHistory() {
    try {
      if (existsSync(this.historyFile)) {
        const data = readFileSync(this.historyFile, 'utf8');
        this.commandHistory = JSON.parse(data);
      }
    } catch (error) {
      console.error('Error loading command history:', error);
    }
  }

  /**
   * Save command history to file
   */
